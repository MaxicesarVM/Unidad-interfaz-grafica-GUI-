    private void listaApellidosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaApellidosValueChanged
     String apellidoSeleccionado = listaApellidos.getSelectedValue();
      

        modelo.setRowCount(0); // limpiamos tabla

        for (Map.Entry<Long, Contacto> entry : frmMenuPrincipal.frmdirectorio.getDirectorio().entrySet()) {
            Contacto c = entry.getValue();

            if (c.getApellido().equalsIgnoreCase(apellidoSeleccionado)) {
                modelo.addRow(new Object[]{
                    c.getDni(),
                    c.getApellido(),
                    c.getNombre(),
                    c.getDireccion(),
                    c.getCiudad(),
                    String.valueOf(entry.getKey())
                        });
            }
        }
    
    }//GEN-LAST:event_listaApellidosValueChanged

