    public void llenarTitulos(){
         modelo.addColumn("Dni");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nombre");
        modelo.addColumn("Direccion");
        modelo.addColumn("Ciudad");
        modelo.addColumn("Telefono");
        
        tablaDatosTelefono.setModel(modelo);
    }
    
public void llenarListaApellidos(){
    String texto = txt_apellidobuscar.getText();
    
    DefaultListModel<String> modeloLista = new DefaultListModel<>();
      for (Contacto c : frmMenuPrincipal.frmdirectorio.getDirectorio().values()) {
          if (c.getApellido().toLowerCase().contains(texto)){
    modeloLista.addElement(c.getApellido());
        }
      }
       listaApellidos.setModel(modeloLista);
    
}
