    private DefaultTableModel modeloTabla = new DefaultTableModel();
    
    private DefaultListModel<String> modelo = new DefaultListModel<>();
    
    
    public void llenarListaTelefonos() {

        
        
        for(Contacto telefono: frmMenuPrincipal.frmdirectorio.getDirectorio().values()){
            
            
            modelo.addElement(telefono.getDni());
            
        }
        
        
       
        
        jlist_listaClientesBorrar.setModel(modelo);
    
        
    
    }
    
    
    
    public void cargarColumnas(){
        
        modeloTabla.addColumn("Dni");
        modeloTabla.addColumn("Apellido");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Direccion");
        modeloTabla.addColumn("Ciudad");
        modeloTabla.addColumn("Telefono");
        
        tbl_tablaClientesBorrar.setModel(modeloTabla);
        
        
        
        
    };
    
    
    
    
    
    
    
    
