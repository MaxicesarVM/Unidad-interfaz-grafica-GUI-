    private void jlist_listaClientesBorrarValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jlist_listaClientesBorrarValueChanged
        
        String dniSeleccionado = jlist_listaClientesBorrar.getSelectedValue();
        txt_dniBorrar.setText(jlist_listaClientesBorrar.getSelectedValue());
        
        modeloTabla.setRowCount(0);
        
        for(Map.Entry<Long, Contacto> cr: frmMenuPrincipal.frmdirectorio.getDirectorio().entrySet()){
            
            if(cr.getValue().getDni().equals(dniSeleccionado)){
                
                modeloTabla.addRow(new Object[]{
                cr.getValue().getDni(),
                cr.getValue().getApellido(),
                cr.getValue().getNombre(),
                cr.getValue().getDireccion(),
                cr.getValue().getCiudad(),
                String.valueOf(cr.getKey())
                });
               
            }
            
            
        }
        
        
    }//GEN-LAST:event_jlist_listaClientesBorrarValueChanged

    private void btn_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_borrarActionPerformed
        
        
        String dniSeleccionadoBorrar = jlist_listaClientesBorrar.getSelectedValue();
        
        
        for(Map.Entry<Long, Contacto> cr: frmMenuPrincipal.frmdirectorio.getDirectorio().entrySet()){
            
            if(cr.getValue().getDni().equals(dniSeleccionadoBorrar)){
                
                frmMenuPrincipal.frmdirectorio.borrarContacto(cr.getKey());
                
                
            }
            
            modelo.removeElement(dniSeleccionadoBorrar);
        }
        
        JOptionPane.showMessageDialog(this, "Se borro el contacto con exito");
        modeloTabla.setRowCount(0);
        
        // tomar en cuenta excepcion al borrar 
        
    }//GEN-LAST:event_btn_borrarActionPerformed

